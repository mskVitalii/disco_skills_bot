ALL_SKILLS: dict[str, SkillDef] = {
    s.name: s
    for s in _INTELLECT + _PSYCHE + _PHYSIQUE + _MOTORICS + _CUSTOM + _SPECIAL
}

SKILL_NAMES: list[str] = list(ALL_SKILLS.keys())

CATEGORIES: dict[str, list[str]] = {}
for _s in ALL_SKILLS.values():
    CATEGORIES.setdefault(_s.category, []).append(_s.name)


def get_skill(name: str) -> SkillDef | None:
    return ALL_SKILLS.get(name)


def skill_list_for_prompt() -> str:
    lines = []
    for cat, names in CATEGORIES.items():
        lines.append(f"\n{cat}:")
        for n in names:
            s = ALL_SKILLS[n]
            lines.append(f"  {s.emoji} {n} — {s.description}")
    return "\n".join(lines)
